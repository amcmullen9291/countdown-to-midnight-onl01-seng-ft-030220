def countdown
  countdown=10
  while countdown>0 do 
     puts"#{countdown} second(S)!"
    countdown-= 1
  end      
     "HAPPY NEW YEAR!"
end

  def countdown_with_sleep
  countdown_with_sleep=10
  while countdown_with_sleep>0 do 
     puts"#{countdown_with_sleep} second(S)!"
    sleep 1 
    countdown_with_sleep-= 1
  end      
     "HAPPY NEW YEAR!"
end